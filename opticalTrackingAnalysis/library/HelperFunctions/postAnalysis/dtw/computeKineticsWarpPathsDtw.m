function captureInfo=computeKineticsWarpPathsDtw(captureInfo)
    [~,captureInfo.kinetics.warpPaths.axialForce] = ...
        dtwK(captureInfo.kinetics.mocap.axialForce',captureInfo.kinetics.ndi.axialForce',@scalarDistance);
    [~,captureInfo.kinetics.warpPaths.bendingMoment] = ...
        dtwK(captureInfo.kinetics.mocap.bendingMoment',captureInfo.kinetics.ndi.bendingMoment',@scalarDistance);
    [~,captureInfo.kinetics.warpPaths.axialMoment] = ...
        dtwK(captureInfo.kinetics.mocap.axialMoment',captureInfo.kinetics.ndi.axialMoment',@scalarDistance);
    [~,captureInfo.kinetics.warpPaths.forces] = ...
        dtwK(captureInfo.kinetics.mocap.forces.vector',captureInfo.kinetics.ndi.forces.vector',@euclideanDistance);
    [~,captureInfo.kinetics.warpPaths.moments] = ...
        dtwK(captureInfo.kinetics.mocap.moments.vector',captureInfo.kinetics.ndi.moments.vector',@euclideanDistance);
    [~,captureInfo.kinetics.warpPaths.thorax.bendingMoment] = ...
        dtwK(captureInfo.kinetics.mocap.thorax.bendingMoment',captureInfo.kinetics.ndi.thorax.bendingMoment',@scalarDistance);
end