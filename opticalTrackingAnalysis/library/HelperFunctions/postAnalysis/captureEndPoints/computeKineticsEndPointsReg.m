        [captureInfo.ndiStart captureInfo.ndiStop];
    captureInfo.kinetics.endPts.bendingMoment=...
        [captureInfo.ndiStart captureInfo.ndiStop];
    captureInfo.kinetics.endPts.axialMoment=...
        [captureInfo.ndiStart captureInfo.ndiStop];
    captureInfo.kinetics.endPts.forces=...
        [captureInfo.ndiStart captureInfo.ndiStop];
    captureInfo.kinetics.endPts.moments=...
        [captureInfo.ndiStart captureInfo.ndiStop];
end
