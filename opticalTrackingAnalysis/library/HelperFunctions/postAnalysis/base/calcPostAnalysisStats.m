function folderStats=calcPostAnalysisStats(folderStats,measure)
    statsStruct=getfield(folderStats,measure{:});
    statsStruct.stats.max(1,:)=mean(statsStruct.max,1);
    statsStruct.stats.max(2,:)=median(statsStruct.max,1);
    statsStruct.stats.max(3,:)=std(statsStruct.max,0,1);
    statsStruct.stats.mae(1,:)=mean(statsStruct.mae,1);
    statsStruct.stats.mae(2,:)=median(statsStruct.mae,1);
    statsStruct.stats.mae(3,:)=std(statsStruct.mae,0,1);
    statsStruct.stats.mean(1,:)=mean(statsStruct.mean,1);
    statsStruct.stats.mean(2,:)=median(statsStruct.mean,1);
    statsStruct.stats.mean(3,:)=std(statsStruct.mean,0,1);
    statsStruct.stats.median(1,:)=mean(statsStruct.median,1);
    statsStruct.stats.median(2,:)=median(statsStruct.median,1);
    statsStruct.stats.median(3,:)=std(statsStruct.median,0,1);
    statsStruct.stats.rms(1,:)=mean(statsStruct.rms,1);
    statsStruct.stats.rms(2,:)=median(statsStruct.rms,1);
    statsStruct.stats.rms(3,:)=std(statsStruct.rms,0,1);
    statsStruct.stats.std(1,:)=mean(statsStruct.std,1);
    statsStruct.stats.std(2,:)=median(statsStruct.std,1);
    statsStruct.stats.mad(1,:)=mean(statsStruct.mad,1);
    statsStruct.stats.mad(2,:)=median(statsStruct.mad,1);
    statsStruct.stats.mad(3,:)=std(statsStruct.mad,0,1);
    statsStruct.stats.topMax(1,:)=mean(statsStruct.topMax,1);
    statsStruct.stats.topMax(2,:)=median(statsStruct.topMax,1);
    statsStruct.stats.topMax(3,:)=std(statsStruct.topMax,0,1);
    statsStruct.stats.diffAtPeaks(1,:)=mean(statsStruct.diffAtPeaks,1);
    statsStruct.stats.diffAtPeaks(2,:)=median(statsStruct.diffAtPeaks,1);
    statsStruct.stats.diffAtPeaks(3,:)=std(statsStruct.diffAtPeaks,0,1);
    
    statsStruct.percentage.max=(statsStruct.max./statsStruct.span)*100;
    statsStruct.percentage.mae=(statsStruct.mae./statsStruct.span)*100;
    statsStruct.percentage.mean=(statsStruct.mean./statsStruct.span)*100;
    statsStruct.percentage.median=(statsStruct.median./statsStruct.span)*100;
    statsStruct.percentage.rms=(statsStruct.rms./statsStruct.span)*100;
    statsStruct.percentage.std=(statsStruct.std./statsStruct.span)*100;
    statsStruct.percentage.mad=(statsStruct.mad./statsStruct.span)*100;
    statsStruct.percentage.topMax=(statsStruct.topMax./repelem(statsStruct.span,1,[5 5 5 5]))*100;
    statsStruct.percentage.diffAtPeaks=(statsStruct.diffAtPeaks./repelem(statsStruct.span,1,[5 5 5 5]))*100;
    
    statsStruct.percentage.stats.max(1,:)=mean(statsStruct.percentage.max,1);
    statsStruct.percentage.stats.max(2,:)=median(statsStruct.percentage.max,1);
    statsStruct.percentage.stats.max(3,:)=std(statsStruct.percentage.max,0,1);
    statsStruct.percentage.stats.mae(1,:)=mean(statsStruct.percentage.mae,1);
    statsStruct.percentage.stats.mae(2,:)=median(statsStruct.percentage.mae,1);
    statsStruct.percentage.stats.mae(3,:)=std(statsStruct.percentage.mae,0,1);
    statsStruct.percentage.stats.mean(1,:)=mean(statsStruct.percentage.mean,1);
    statsStruct.percentage.stats.mean(2,:)=median(statsStruct.percentage.mean,1);
    statsStruct.percentage.stats.mean(3,:)=std(statsStruct.percentage.mean,0,1);
    statsStruct.percentage.stats.median(1,:)=mean(statsStruct.percentage.median,1);
    statsStruct.percentage.stats.median(2,:)=median(statsStruct.percentage.median,1);
    statsStruct.percentage.stats.median(3,:)=std(statsStruct.percentage.median,0,1);
    statsStruct.percentage.stats.rms(1,:)=mean(statsStruct.percentage.rms,1);
    statsStruct.percentage.stats.rms(2,:)=median(statsStruct.percentage.rms,1);
    statsStruct.percentage.stats.rms(3,:)=std(statsStruct.percentage.rms,0,1);
    statsStruct.percentage.stats.std(1,:)=mean(statsStruct.percentage.std,1);
    statsStruct.percentage.stats.std(2,:)=median(statsStruct.percentage.std,1);
    statsStruct.percentage.stats.mad(1,:)=mean(statsStruct.percentage.mad,1);
    statsStruct.percentage.stats.mad(2,:)=median(statsStruct.percentage.mad,1);
    statsStruct.percentage.stats.topMax(1,:)=mean(statsStruct.percentage.topMax,1);
    statsStruct.percentage.stats.topMax(2,:)=median(statsStruct.percentage.topMax,1);
    statsStruct.percentage.stats.topMax(3,:)=std(statsStruct.percentage.topMax,0,1);
    statsStruct.percentage.stats.diffAtPeaks(1,:)=mean(statsStruct.percentage.diffAtPeaks,1);
    statsStruct.percentage.stats.diffAtPeaks(2,:)=median(statsStruct.percentage.diffAtPeaks,1);
    statsStruct.percentage.stats.diffAtPeaks(3,:)=std(statsStruct.percentage.diffAtPeaks,0,1);
    
    statsStruct.stats.span(1,:)=mean(statsStruct.span,1);
    statsStruct.stats.span(2,:)=median(statsStruct.span,1);
    statsStruct.stats.span(3,:)=std(statsStruct.span,0,1);
    folderStats=setfield(folderStats,measure{:},statsStruct);
end